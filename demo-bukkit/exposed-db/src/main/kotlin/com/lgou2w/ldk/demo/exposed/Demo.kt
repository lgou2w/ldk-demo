package com.lgou2w.ldk.demo.exposed

import org.jetbrains.exposed.dao.EntityID
import org.jetbrains.exposed.dao.IntEntity
import org.jetbrains.exposed.dao.IntEntityClass
import org.jetbrains.exposed.dao.IntIdTable

object Demos : IntIdTable(columnName = "exposedDb") {
    val name = varchar("name", 16).uniqueIndex()
    val value = integer("value").default(0)
}

class Demo(id: EntityID<Int>) : IntEntity(id) {
    companion object : IntEntityClass<Demo>(Demos)
    var name by Demos.name
    var value by Demos.value
}
